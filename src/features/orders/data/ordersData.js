// Helper to calculate mock dates relative to current run time
function getDynamicDateString(daysAgo) {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  const d = String(date.getDate()).padStart(2, "0");
  const m = monthNames[date.getMonth()];
  const y = date.getFullYear();
  return `${d} ${m} ${y} 12:30 PM`;
}

